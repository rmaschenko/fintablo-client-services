  // ── Проекция Финтабло-возврата ──────────────────────────
  // По нашим данным, после системного управленческого учёта
  // за 3 мес возвращается 40–60% разрыва (формула позаимствована
  // из money-profit/js/calculator.js:376-381).
  function fintabloProjection(estimatedAnnualLoss) {
    if (!estimatedAnnualLoss || estimatedAnnualLoss <= 0) {
      return { low: 0, high: 0, midpoint: 0 };
    }
    return {
      low:      Math.round(estimatedAnnualLoss * 0.40),
      high:     Math.round(estimatedAnnualLoss * 0.60),
      midpoint: Math.round(estimatedAnnualLoss * 0.50)
    };
  }

