    const pain = inputs.primaryPain;
    const profileCode = classifyProfile(inputs);
    const profile = PROFILES[profileCode];
    const questionsRaw = PAIN_QUESTIONS[pain] || PAIN_QUESTIONS.no_big_picture;
    const questions = questionsRaw.map(q => substitute(q, unit));

