  function formatMoney(n) {
    if (!n && n !== 0) return '—';
    const abs = Math.abs(Math.round(n));
    return abs.toString().replace(/\B(?=(\d{3})+(?!\d))/g, '\u00A0') + ' ₽';
  }

  function formatMoneyCompact(n) {
    if (!n && n !== 0) return '—';
    const abs = Math.abs(n);
    // \u00A0 — неразрывный пробел: «2,3 млн ₽» никогда не переносится по пробелу
    if (abs >= 1_000_000_000) {
      const b = abs / 1_000_000_000;
      return (b >= 10 ? Math.round(b) : b.toFixed(1).replace('.', ',').replace(',0', '')) + '\u00A0млрд\u00A0₽';
    }
    if (abs >= 1_000_000) {
      const m = abs / 1_000_000;
      return (m >= 10 ? Math.round(m) : m.toFixed(1).replace('.', ',').replace(',0', '')) + '\u00A0млн\u00A0₽';
    }
    if (abs >= 1000) return Math.round(abs / 1000) + '\u00A0тыс\u00A0₽';
    return abs + '\u00A0₽';
  }

  function plural(n, forms) {
    const mod10 = n % 10, mod100 = n % 100;
    if (mod10 === 1 && mod100 !== 11) return forms[0];
    if (mod10 >= 2 && mod10 <= 4 && (mod100 < 10 || mod100 >= 20)) return forms[1];
    return forms[2];
  }

