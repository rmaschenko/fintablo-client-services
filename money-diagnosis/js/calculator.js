  // Бенчмарк индекса: где примерно находится когорта (аналогичные по масштабу и системе)
  function peerIndexFor(monthlyRevenue, accountingSystem) {
    const base = SYSTEM_SCORE[accountingSystem] || 0;
    // В масштабе 10М+ "базовая" система, как правило, уже не справляется — 5 поинтов вниз
    const scaleAdj = monthlyRevenue >= 10_000_000 ? -5 : 0;
    return Math.max(10, Math.min(90, base + scaleAdj + 10)); // +10 — типичная средняя когорта выше минимума системы
  }

